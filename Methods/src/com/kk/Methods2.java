package com.kk;

public class Methods2 {

	public static void main(String[] args) {
firstPattern();
secondPattern();
thirdPattern();
	}
	
	public static void firstPattern() {
		System.out.println("*");
		System.out.println("* *");
		System.out.println("* * *");
		System.out.println("* * * *");
		System.out.println("* * * * *");
		System.out.println("\n\n\n");
	}
	
	public static void secondPattern() {
		System.out.println("* * * * *");
		System.out.println("* * * * ");
		System.out.println("* * * ");
		System.out.println("* * ");
		System.out.println("* ");
		System.out.println("\n\n\n");
	}
	
	public static void thirdPattern() {
		System.out.println("        *");
		System.out.println("      * *");
		System.out.println("    * * *");
		System.out.println("  * * * *");
		System.out.println("* * * * *");
	}

}
