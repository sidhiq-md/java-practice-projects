package com.kk;

public class UnaryOperator {
	public static void main (String[] args) {
	int y = 10;
	int x = 0;
	x = -y;
	System.out.println("x = " + x);
	x = ++y;
System.out.println(x);
System.out.println(x++);
System.out.println(x);
System.out.println(++x);

System.out.println(x++);
System.out.println(x);
System.out.println(x++);
System.out.println(x++);

	}
}
